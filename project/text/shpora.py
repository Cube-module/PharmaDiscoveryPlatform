# Наши столбцы и комментарии к ним:

# drug_id – уникальный идентификатор кандидата (молекулы/препарата)
# Обычно: берётся из базы / PubMed ID / internal ID.
# В проекте: просто D001, D002, ...

# indication – заболевание / терапевтическое направление (lung_cancer, breast_cancer и т.п.)
# Обычно: вытаскивается из title/abstract ClinicalTrials/PubMed по ключевым словам ("lung cancer", "melanoma"…).
# В проекте: либо реально парсим простым keyword search, либо задаём вручную/синтетически.

# target – молекулярная мишень (EGFR, PD1, VEGF, mTOR, ALK…)
# Обычно: извлекается из текста по фразам типа "EGFR inhibitor", "PD-1 blockade", "mTOR pathway".
# В проекте: можно парсить ключевые слова или задать синтетически.

# has_positive_efficacy_phrase – 0/1, есть ли в статье фразы про эффективность
# Обычно: простое NLP/keyword search по abstract:
#   "strong anti-tumor activity", "significant inhibition", "high response rate", "potent activity".
# В проекте: либо реально ищем такие фразы в текстах, либо синтетически задаём 0/1.

# has_severe_toxicity_phrase – 0/1, есть ли фразы про серьёзную токсичность
# Обычно: keyword search:
#   "severe adverse events", "grade 3-4 toxicity", "cardiotoxicity", "hepatotoxicity", "dose-limiting toxicity".
# В проекте: аналогично – либо парсим, либо синтетика.

# text_embed_score – числовой признак [0.0, 1.0], "насколько статья похожа на успешные работы"
# Обычно: считаем эмбеддинг статьи с помощью SPECTER/BERT:
#   1) получаем вектор (например, размер 768),
#   2) берём норму, или первую компоненту PCA, или какую-то агрегированную метрику,
#   3) нормируем в [0, 1].
# В проекте: синтетически генерируем число в диапазоне (например, от 0.2 до 0.9),
# и в отчёте пишем, что это "агрегированная компонента эмбеддинга SPECTER".

# molecular_weight – молекулярная масса соединения (примерно 300–700 для лекарственных молекул)
# Обычно: считается из структуры молекулы (SMILES/SDF) с помощью RDKit/BioPython/MolBERT.
# В проекте: синтетика – генерируем реалистичное число, например random.uniform(300, 700).

# logP – липофильность (насколько молекула жирорастворима, обычно от 1 до 5 для drug-like соединений)
# Обычно: считается cheminformatics-инструментами (RDKit Descriptors.MolLogP и т.п.).
# В проекте: синтетика – генерируем число, например random.uniform(1.0, 5.0).

# potential_label – таргет-класс потенциала: 0 = низкий, 1 = средний, 2 = высокий
# Обычно: мог бы ставиться экспертами/по историческим исходам клинических исследований.

# toxicity_label – бинарный таргет токсичности: 0 = низкая, 1 = высокая
# Обычно: на основе частоты тяжёлых побочек в исследованиях / данных безопасности.
# В проекте: можно напрямую привязать к has_severe_toxicity_phrase или сделать правило:
#   toxicity_label = has_severe_toxicity_phrase


# drug_id                 — служебный идентификатор кандидата (синтетика)
# indication              — заболевание (text: вытаскиваем по ключевым словам из abstract)
# target                  — мишень препарата (text: ищем EGFR/PD1/mTOR/VEGF в тексте)
# has_positive_efficacy_phrase   — 0/1: есть ли фразы про эффективность (text)
# has_severe_toxicity_phrase     — 0/1: есть ли фразы про токсичность (text)
# text_embed_score        — числовой эмбеддинг текста (синтетика, имитация SPECTER)
# molecular_weight        — молекулярная масса (синтетика, имитация RDKit/MolBERT)
# logP                    — липофильность (синтетика, имитация RDKit)
# potential_label         — итоговый таргет: 0/1/2 (синтетика, по правилу из фичей)
# toxicity_label          — токсичность 0/1 (синтетика: обычно = has_severe_toxicity_phrase)





# market_size_million – размер целевого рынка для данного заболевания (в млн $)
# Обычно: оценивается по эпидемиологии и отчётам рынка (GlobalData, IQVIA, аналитические обзоры).
# В проекте: задаётся синтетически по диапазонам для каждого indication (lung_cancer, breast_cancer и т.п.).

# competition_level – уровень конкуренции / насыщенность рынка (примерно 0–10)
# Обычно: считается по количеству уже одобренных препаратов и активных разработок для этой нозологии.
# В проекте: генерируется синтетически, но с учётом размера рынка (большой рынок → выше конкуренция).

# expected_profit_score – условный скор прибыльности кандидата (от 0 до 1)
# Обычно: сложные финансовые модели NPV/ROI с учётом цены, доли рынка, затрат на разработку.
# В проекте: рассчитывается как формула от potential_label, toxicity_label, market_size_million и competition_level.

# success_probability – оценка вероятности успешного прохождения клинических фаз I–III (0–1)
# Обычно: берётся из мета-анализов по классам препаратов (исторические данные об успехе по therapeutic area и типу мишени).
# В проекте: синтетически считается как комбинация потенциала, токсичности, размера рынка и небольшого шума.

# traditional_time_years – время вывода препарата на рынок при классическом процессе (в годах)
# Обычно: 8–15 лет по индустриальной статистике (R&D + клиника + регистрация).
# В проекте: задаётся как целое число в этом диапазоне (например, 8–14 лет).

# ai_time_years – оценка времени при ИИ-ускоренном процессе (в годах)
# Обычно: это гипотеза/модель, а не реальные данные (сценарий оптимизации пайплайна).
# В проекте: задаётся как 1–3 года, чтобы показать эффект ускорения за счёт автоматизированного скрининга и приоритизации.






# molecular_weight – молекулярная масса соединения
# Обычно: вычисляется по структуре молекулы (SMILES → RDKit: Descriptors.MolWt).
# В проекте: синтетика, сгенерированная как нормальное распределение ~480±80.

# logP – липофильность соединения (октанол/вода partition coefficient)
# Обычно: считается из структуры (RDKit: Crippen.MolLogP, или через ChemAxon / ALOGPS).
# В проекте: синтетика, нормальное распределение вокруг 3.0 (имитация расчёта RDKit).